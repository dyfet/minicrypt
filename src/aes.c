static void inv_sub_bytes(uint8_t state[16]) {
    for (int i = 0; i < 16; ++i)
        state[i] = inv_sbox[state[i]];
}

static uint8_t xtime(uint8_t x) {
    return (x << 1) ^ ((x >> 7) * 0x1B);
}

static void mix_columns(uint8_t state[16]) {
    for (int i = 0; i < 4; ++i) {
        uint8_t *col = &state[i * 4];
        uint8_t a = col[0], b = col[1], c = col[2], d = col[3];
        col[0] = xtime(a) ^ xtime(b) ^ b ^ c ^ d;
        col[1] = a ^ xtime(b) ^ xtime(c) ^ c ^ d;
        col[2] = a ^ b ^ xtime(c) ^ xtime(d) ^ d;
        col[3] = xtime(a) ^ a ^ b ^ c ^ xtime(d);
    }
}

static uint8_t mul(uint8_t x, uint8_t y) {
    uint8_t r = 0;
    for (; y; y >>= 1) {
        if (y & 1) r ^= x;
        x = xtime(x);
    }
    return r;
}

static void inv_mix_columns(uint8_t state[16]) {
    for (int i = 0; i < 4; ++i) {
        uint8_t *col = &state[i * 4];
        uint8_t a = col[0], b = col[1], c = col[2], d = col[3];
        col[0] = mul(a, 0x0E) ^ mul(b, 0x0B) ^ mul(c, 0x0D) ^ mul(d, 0x09);
        col[1] = mul(a, 0x09) ^ mul(b, 0x0E) ^ mul(c, 0x0B) ^ mul(d, 0x0D);
        col[2] = mul(a, 0x0D) ^ mul(b, 0x09) ^ mul(c, 0x0E) ^ mul(d, 0x0B);
        col[3] = mul(a, 0x0B) ^ mul(b, 0x0D) ^ mul(c, 0x09) ^ mul(d, 0x0E);
    }
}

bool mc_aes_setup(mc_aes_ctx *ctx, uint8_t *key, mc_aes_keysize_t size) {
    if (!ctx || !key) return false;
    int Nk, Nr, i;
    uint32_t temp;
    mc_aes_clear(ctx);
    switch (size) {
    case MC_AES_128:
        Nk = 4;
        Nr = 10;
        break;
    case MC_AES_192:
        Nk = 6;
        Nr = 12;
        break;
    case MC_AES_256:
        Nk = 8;
        Nr = 14;
        break;
    default:
        return false;
    }
    ctx->rounds = Nr;
    ctx->keysize = size;
    for (i = 0; i < Nk; ++i) {
        ctx->keyrounds[i] = PACK_BE32(&key[4 * i]);
    }

    for (i = Nk; i < 4 * (Nr + 1); ++i) {
        temp = ctx->keyrounds[i - 1];
        if (i % Nk == 0) {
            temp = subword(rotl(temp)) ^ (rcon[(i / Nk) - 1] << 24);
        } else if (Nk > 6 && i % Nk == 4) {
            temp = subword(temp);
        }
        ctx->keyrounds[i] = ctx->keyrounds[i - Nk] ^ temp;
    }
    return true;
}

void mc_aes_clear(mc_aes_ctx *ctx) {
    if (ctx)
        memset(ctx, 0, sizeof(mc_aes_ctx));
}

void mc_aes_encrypt(mc_aes_ctx *ctx, const uint8_t *in, uint8_t *out) {
    if (!ctx || !ctx->rounds) return;
    uint8_t state[16];
    memcpy(state, in, 16);
    add_round_key(state, &ctx->keyrounds[0]);
    for (int round = 1; round < ctx->rounds; ++round) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, &ctx->keyrounds[round * 4]);
    }

    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, &ctx->keyrounds[ctx->rounds * 4]);
    minicrypt_memcpy(out, state, 16);
}

void mc_aes_decrypt(mc_aes_ctx *ctx, const uint8_t *in, uint8_t *out) {
    if (!ctx || !ctx->rounds) return;
    uint8_t state[16];
    memcpy(state, in, 16);
    add_round_key(state, &ctx->keyrounds[ctx->rounds * 4]);
    for (int round = ctx->rounds - 1; round > 0; --round) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(state, &ctx->keyrounds[round * 4]);
        inv_mix_columns(state);
    }

    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(state, &ctx->keyrounds[0]);
    minicrypt_memcpy(out, state, 16);
}
