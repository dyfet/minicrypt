static uint32_t rotl(uint32_t w) {
    return (w << 8) | (w >> 24);
}

static uint32_t subword(uint32_t w) {
    return (sbox[(w >> 24) & 0xFF] << 24) |
           (sbox[(w >> 16) & 0xFF] << 16) |
           (sbox[(w >> 8) & 0xFF] << 8) |
           (sbox[w & 0xFF]);
}

static void sub_bytes(uint8_t state[16]) {
    for (int i = 0; i < 16; ++i)
        state[i] = sbox[state[i]];
}

static void shift_rows(uint8_t state[16]) {
    uint8_t tmp[16];

    tmp[0] = state[0];
    tmp[4] = state[4];
    tmp[8] = state[8];
    tmp[12] = state[12];

    tmp[1] = state[5];
    tmp[5] = state[9];
    tmp[9] = state[13];
    tmp[13] = state[1];

    tmp[2] = state[10];
    tmp[6] = state[14];
    tmp[10] = state[2];
    tmp[14] = state[6];

    tmp[3] = state[15];
    tmp[7] = state[3];
    tmp[11] = state[7];
    tmp[15] = state[11];
    minicrypt_memcpy(state, tmp, 16);
}

static void add_round_key(uint8_t state[16], const uint32_t *round_key) {
    for (int i = 0; i < 4; ++i) {
        uint32_t rk = round_key[i];
        state[4 * i + 0] ^= (rk >> 24) & 0xFF;
        state[4 * i + 1] ^= (rk >> 16) & 0xFF;
        state[4 * i + 2] ^= (rk >> 8) & 0xFF;
        state[4 * i + 3] ^= rk & 0xFF;
    }
}

